# Bitwise Operator

a = 23
b = 14
result = 0

# Bitwise AND
result = a & b
print(result)

# Bitwise OR
result = a | b
print(result)

# Bitwise XOR
result = a ^ b
print(result)

# Bitwise NOT
result = ~a
print(result)

# Left Shift Bitwise Operators
result = a << 3
print(result)

# Right Shift Bitwise Operators
result = a >> 3
print(result)